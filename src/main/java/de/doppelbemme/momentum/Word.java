package de.doppelbemme.momentum;

import java.util.List;

public class Word {
    private int length;
    private List<String> words;

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public List<String> getWords() {
        return words;
    }

    public void setWords(List<String> words) {
        this.words = words;
    }
}
